import sqlite3

try:
    conn = sqlite3.connect('db.sqlite3')
    cursor = conn.cursor()
    cursor.execute("SELECT name FROM sqlite_master WHERE type='table';")
    print('Tabelas existentes:')
    for row in cursor.fetchall():
        print(f"  - {row[0]}")
    conn.close()
except Exception as e:
    print(f'Erro: {e}')


